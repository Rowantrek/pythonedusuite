	def RegisterCheck(self):
		username = self.guif_rm_input_user.text()
		password = self.guif_rm_input_pass.text()
		email = self.guif_rm_input_email.text()
		date_of_birth = self.guif_rm_input_date.text()
		#CHECK THAT INPUTS ARE VALID
		if (len(username) <= 0):
			self.RegInvalid()
		if (len(password) <= 0):
			self.RegInvalid()
		if (len(email) <= 4):
			self.RegInvalid()
		if (len(date_of_birth) != 10):
			self.RegInvalid()
		#PASS INPUTS TO REGISTER CHECK EDU FUNC
		check = edufunctions.RDB.Register(username,password,email,date_of_birth)
		if (check):
			self.guif_rm_label_invalid.setText("Account Created")
			self.guif_rm_label_invalid.setStyleSheet("color: rgb(0, 255, 0);")
			self.guif_rm_label_invalid.show()
